function str = numbers(x)
switch x
    case 1
        str = 'one';
    case 2
        str = 'two';
    case 3
        str = 'three';
    case 4
        str = 'four';
    case 5
        str = 'five';
    case 6
        str = 'six';
    case 7
        str = 'seven';
    case 8
        str = 'eight';
    case 9
        str = 'nine';
    otherwise
        str = 'more than 9';
end